#define MAGIC_MATRIX ((uint32_t)0xFF000011U)
#define MAGIC_STRING ((uint32_t)0xFF000022U)

#define MAGIC_WORLD ((uint32_t)0xEE111111U)
#define MAGIC_SKELETON ((uint32_t)0xEE222222U)
#define MAGIC_BODY ((uint32_t)0xEE333333U)
#define MAGIC_SHAPE ((uint32_t)0xEE444444U)
#define MAGIC_ENTITY ((uint32_t)0xEE555555U)

#define MAGIC_SHAPE_NAY ((uint32_t)0xDD000000U)
#define MAGIC_SHAPE_YEA ((uint32_t)0xDD111111U)


struct RemotelySimulatedDartVisual
{
   FMatrix matrix;
   FString shape;
   float param0, param1, param2, param3, param4, param5, param6, param7;
};

class Deserializer
{
public:
   Deserializer(const TSharedPtr<FBufferReader> reader) : reader(reader) {}

   TArray<RemotelySimulatedDartVisual> Deserialize()
   {
      TArray<RemotelySimulatedDartVisual> result;

      EatMagic(MAGIC_WORLD);

      uint32 numSkeletons;
      *reader << numSkeletons;

      for (auto i = 0U; i < numSkeletons; i++)
      {
         VisitSkeleton(result, FMatrix::Identity);
      }

      return result;
   }


   void VisitSkeleton(TArray<RemotelySimulatedDartVisual>& result, const FMatrix transform)
   {
      EatMagic(MAGIC_SKELETON);

      uint32 numTrees;
      *reader << numTrees;

      for (auto i = 0U; i < numTrees; i++)
      {
         VisitBody(result, transform);
      }
   }

   void VisitBody(TArray<RemotelySimulatedDartVisual>& result, const FMatrix transform)
   {
      EatMagic(MAGIC_BODY);

      auto mx = ReadMatrix();
      auto transformed = mx * transform;

      uint32 numShapes;
      *reader << numShapes;

      for (auto i = 0U; i < numShapes; i++)
      {
         VisitShape(result, transformed);
      }

      uint32 numEntities;
      *reader << numEntities;

      for (auto i = 0U; i < numEntities; i++)
      {
         VisitEntity(result, transformed);
      }
   }

   void VisitShape(TArray<RemotelySimulatedDartVisual>& result, const FMatrix transform)
   {
      EatMagic(MAGIC_SHAPE);

      uint32 yeaOrNay;
      *reader << yeaOrNay;

      if (yeaOrNay == MAGIC_SHAPE_NAY)
      {
         return;
      }

      auto mx = ReadMatrix();
      auto transformed = mx * transform;

      uint32 shapeNameChars;
      *reader << shapeNameChars;
      check(shapeNameChars < 32);

      auto bytes = new uint8_t[shapeNameChars + 1];
      for (auto i = 0U; i < shapeNameChars; i++)
      {
         uint8_t val;
         *reader << val;
         bytes[i] = val;
      }
      bytes[shapeNameChars] = 0;

      FString shapeName{ UTF8_TO_TCHAR(bytes) };

      RemotelySimulatedDartVisual res = {};
      res.matrix = transformed;
      res.shape = shapeName;

      if (res.shape == TEXT("BoxShape") || res.shape == TEXT("EllipsoidShape")) {
         *reader << res.param0;
         *reader << res.param1;
         *reader << res.param2;
      } else if (res.shape == TEXT("MeshShape")) {
         *reader << res.param0;
         *reader << res.param1;
         *reader << res.param2;
         *reader << res.param3;
         *reader << res.param4;
         *reader << res.param5;
      } else {
         throw std::runtime_error(TCHAR_TO_UTF8(*res.shape));
      }

      // *reader << res.param6;
      // *reader << res.param7;

      result.Add(res);
   }

   void VisitEntity(TArray<RemotelySimulatedDartVisual>& result, const FMatrix transform)
   {
      EatMagic(MAGIC_ENTITY);
      
      uint32 isBodyNode;
      *reader << isBodyNode;

      if (isBodyNode)
      {
         VisitBody(result, transform);
      }

      uint32 isShapeFrame;
      *reader << isShapeFrame;

      if (isShapeFrame)
      {
         VisitShape(result, transform);
      }
   }


private:
   FMatrix ReadMatrix() {
      EatMagic(MAGIC_MATRIX);

      FMatrix mx;
      for (auto row = 0; row < 4; row++)
      {
         for (auto col = 0; col < 4; col++)
         {
            float val;
            *reader << val;
            mx.M[row][col] = val;
         }
      }
      return mx;
   }

   void EatMagic(uint32 magic)
   {
      uint32 val;
      *reader << val;
      check(val == magic);
   }

   TSharedPtr<FBufferReader> reader;
};
